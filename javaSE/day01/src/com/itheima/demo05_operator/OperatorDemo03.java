package com.itheima.demo05_operator;

public class OperatorDemo03 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println("-----------");
        b += a;
        System.out.println(b);
        System.out.println("-----------");
        b -=a;
        System.out.println(b);
        System.out.println("-----------");
        b *=a;
        System.out.println(b);
        System.out.println("-----------");
        b /=a;
        System.out.println(b);
        System.out.println("-----------");
        b %=a;
        System.out.println(b);
        System.out.println("-----------");
    }
}
