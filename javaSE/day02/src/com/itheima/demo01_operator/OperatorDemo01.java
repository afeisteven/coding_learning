package com.itheima.demo01_operator;

public class OperatorDemo01 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println("---------");
        System.out.println(a=b);
    }
}
